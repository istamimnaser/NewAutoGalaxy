export const formatPrice = (price: number): string => {
  if (price >= 10000000) return `৳${(price / 10000000).toFixed(1)} Cr`;
  if (price >= 100000) return `৳${(price / 100000).toFixed(1)} Lac`;
  return `৳${price.toLocaleString()}`;
};
